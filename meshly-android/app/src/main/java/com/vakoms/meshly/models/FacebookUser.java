public class FacebookUser {
   public String facebookId;
   public String email;
   public String accessToken;
   public String image;
   public String name;

    public String getFacebookId() {
        return facebookId;
    }



    public String getEmail() {
        return email;
    }



    public String getAccessToken() {
        return accessToken;
    }



    public String getImage() {
        return image;
    }



    public String getName() {
        return name;
    }


}
